# Main Function
#==============================================================================
def calccoef(nordem,ndim):

    ntotal = nordem + 1
    
    vcoef = np.zeros(ntotal)
    
    nmeio = int(0.5*ntotal)
        
    c = np.zeros(nmeio+1)
    
    if(nordem==6):
        
        if(ndim==1):
                    
            c[1] =  1.56808208
            c[0] =  (-4/3)*c[1] + (-13/18) 
            c[2] =  (9/20)      + (-2/5)*c[1] 
            c[3] =  (3/45)*c[1] + (-4/45)
        
    vcoef[nmeio] = c[0]   
 
    for i in range(0,nmeio):
        
        vcoef[i]          = c[nmeio-i] 
        vcoef[ntotal-i-1] = c[nmeio-i]
            
    return vcoef
