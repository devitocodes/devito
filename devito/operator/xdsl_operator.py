            module_obj = transform_devito_to_iet_ssa(self)

            iet_to_standard_mlir(module_obj)

            module_str = StringIO()
            Printer(target=Printer.Target.MLIR, stream=module_str).print(module_obj)
            module_str = module_str.getvalue()

            f = self._tf.name

